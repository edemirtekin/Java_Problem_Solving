public class Q22 {

    // (TR) 100'den 0'a kadar çift sayıları yazdırın, ancak decrement(i--) kullanmayın.
    // (EN) Print even numbers from 100 to 0, but do not use decrement(i--).

    public static void main(String[] args) {

        for (int i=0; i<=50; i++){
            System.out.print(2*(50-i) + " ");
        }

    }

}
